/*******************************************************************************
* Function Name: ADC_1_Stop
****************************************************************************//**
*
* \brief This function stops ADC conversions and puts the ADC into its lowest 
* power mode.
* 
* \param None
* 
* \return None
* 
* \sideeffect
* Do not use the ADC_1_Stop() API to halt conversions. Instead use the 
* ADC_1_StopConvert() API. If you use the ADC_1_Stop() API 
* to halt conversions then later use the ADC_1_Start() and 
* ADC_1_StartConvert() APIs to resume conversions, the first channel 
* of the scan may be corrupt. The ADC_1_StopConvert() API will enable 
* the ADC_1 to complete the current scan of channels. After the 
* channel scan is complete, the ADC_1 will stop all conversions, which 
* can be detected by the use of an ISR or the ADC_1_IsEndConversion() 
* flag.
*
* <b>Code Snippet</b>
*  \snippet main.c snippet_ADC_1_Stop
*
*******************************************************************************/
void ADC_1_Stop(void)
{	
    #if(0u != ADC_1_ANY_CONFIG_USES_FILTER)	
    	if((ADC_1_allConfigs[ADC_1_currentConfig].miscConfig 
    	   & ADC_1_MISC_CONFIG_FILTER_PRESENT_MSK) 
    	   == ADC_1_MISC_CONFIG_FILTER_PRESENT_MSK)
    	{
    		ADC_1_UABH_A_Stop();
    		ADC_1_UABH_B_Stop();
            ADC_1_FILTERAGND2SAR_BUFFER_Stop();
    	}
    #endif /* ADC_1_ANY_CONFIG_USES_FILTER != 0u */
    ADC_1_SAR_CTRL_REG &= (uint32)~ADC_1_SAR_CTRL_ENABLED_MSK;
    return;
}

/* ****************************************************************************
* Function Name: ADC_1_InitConfig
****************************************************************************//*
*
* \brief Configures all of the registers for a given configuration for scanning.
*
* \param scan Number of scan defined in the ADC_1.
*
* \return None
*
* \sideeffect None
*
* <b>Code Snippet</b>
*  \snippet main.c snippet_ADC_1_InitConfig
*
*******************************************************************************/
void ADC_1_InitConfig(const ADC_1_CONFIG_STRUCT *config)
{
	uint32 chNum;
    uint32 tmpRegVal;
	
    #if(0u != ADC_1_ANY_CONFIG_USES_FILTER)	        
    	if((config->miscConfig & ADC_1_MISC_CONFIG_FILTER_PRESENT_MSK) 
    	    == ADC_1_MISC_CONFIG_FILTER_PRESENT_MSK)
    	{
            ADC_1_intUabClock_SetFractionalDividerRegister(config->filterClkDivider, 0u);
            
    	}
    #endif /* ADC_1_ANY_CONFIG_USES_FILTER != 0u */
    #if (ADC_1_CLOCK_INTERNAL)
        ADC_1_intSarClock_SetFractionalDividerRegister(config->clkDivider, 0u);
        
    #endif /* ADC_1_CLOCK_INTERNAL */
    /* Init SAR and MUX registers */
	ADC_1_SAR_CTRL_REG = config->ctrl
   
	/* Enable the SAR internal pump when global pump is enabled */
    | (((ADC_1_PUMP_CTRL_REG & ADC_1_PUMP_CTRL_ENABLED) != 0u) 
    ? ADC_1_SAR_CTRL_BOOSTPUMP_EN_MSK : 0u);
	
	ADC_1_SAR_SAMPLE_CTRL_REG = config->sampleCtrl;
	ADC_1_SAR_RANGE_THRES_REG = config->rangeThres;	
	ADC_1_SAR_RANGE_COND_REG = config->rangeCond;
	ADC_1_SAR_SAMPLE_TIME01_REG = config->sampleTime01;
	ADC_1_SAR_SAMPLE_TIME23_REG = config->sampleTime23;
    
	/* Connect Vminus to VSSA when even one channel is single-ended or multiple channels configured */
	if(1u == ADC_1_MUX_SWITCH0_INIT)
	{
		ADC_1_MUX_SWITCH0_REG |= ADC_1_MUX_FW_VSSA_VMINUS;
		/* Set MUX_HW_CTRL_VSSA in MUX_SWITCH_HW_CTRL when multiple channels enabled */
		if(1ul < config->numChannels)
		{
			ADC_1_MUX_SWITCH_HW_CTRL_REG |= ADC_1_MUX_FW_VSSA_VMINUS;
		}
	}
    
	/* Clear and set interrupt masks */
	ADC_1_SAR_SATURATE_INTR_MASK_REG = 0u;
    ADC_1_SAR_RANGE_INTR_MASK_REG = 0u;	
	ADC_1_SAR_SATURATE_INTR_MASK_REG = config->satIntMask;
	ADC_1_SAR_RANGE_INTR_MASK_REG = config->rangeIntMask;
	ADC_1_SAR_INTR_MASK_REG = ADC_1_EOS_MASK;
    
	/* Double the non-overlap delay and latch enable time when using */
    /* internal reference without bypass cap */
    tmpRegVal = ADC_1_SAR_DFT_CTRL_REG;
	if(((config->ctrl & ADC_1__SAR_CTRL_VREF_BYP_AND_SEL_MSK) 
	    == ADC_1__INTERNAL1024) 
	    || ((config->ctrl & ADC_1__SAR_CTRL_VREF_BYP_AND_SEL_MSK) 
	    == ADC_1__INTERNALVREF))
	{
        tmpRegVal &= ~ADC_1_SAR_DFT_CTRL_DCEN_MSK;
		tmpRegVal |= ADC_1_SAR_DFT_CTRL_DLY_INC_MSK;
	}
	else
	{
		tmpRegVal |= ADC_1_SAR_DFT_CTRL_DCEN_MSK;
        tmpRegVal &= (uint32)~ADC_1_SAR_DFT_CTRL_DLY_INC_MSK;
	}	
	ADC_1_SAR_DFT_CTRL_REG = tmpRegVal;
	
	for(chNum = 0u; chNum < config->numChannels; chNum++)
    {
		tmpRegVal = (ADC_1_channelsConfig[config->channelBase + chNum] 
		& ADC_1_CHANNEL_CONFIG_MASK);
        
        #if(ADC_1_TOTAL_CHANNELS_NUM > 1)
            tmpRegVal |= ADC_1_InputsPlacement[config->channelBase + chNum];
            /*Non-paired differential channels set NEG_ADDR_EN and clear DIFFERENTIAL_EN */
            if(0u != (ADC_1_InputsPlacement[config->channelBase + chNum] & ADC_1_CHANNEL_CONFIG_NEG_ADDR_EN_MSK))
            {
                tmpRegVal &= ~ADC_1_SAR_CHAN_CONFIG_DIFFERENTIAL_EN_MSK;
            }
        #endif
        
		CY_SET_REG32((reg32 *)(ADC_1_SAR_CHAN_CONFIG_IND + (uint32)(chNum << 2)), tmpRegVal);
	}
	
    return;
}

/* ****************************************************************************
* Function Name: ADC_1_SelectConfig
****************************************************************************//* 
*
* \brief Selects the predefined configuration for scanning. Disables
* and re-enables the SAR and filter (if filter used).
* 
* \param config Number of configuration  in the ADC_1.
* 
* \param restart Determines if the ADC_1 should be  restarted after 
* selecting the configuration.
**
* <b>Code Snippet</b>
*  \snippet main.c snippet_ADC_1_SelectConfig
*
*******************************************************************************/
void ADC_1_SelectConfig(uint32 config, uint32 restart)
{
    /* Check whether the configuration number is valid or not */
    if(ADC_1_TOTAL_CONFIGS > config)
    {        
        /* Stop the ADC before changing configurations */
	    ADC_1_Stop();
        ADC_1_selected = 1u;
        
    	if(0u == ADC_1_initVar)
    	{
            ADC_1_Init();
            ADC_1_initVar |= ADC_1_INIT_VAR_INIT_FLAG;
    	}
        #if (ADC_1_VREF_ROUTED)
            ADC_1_vrefAMux_DisconnectAll();
        #endif
    	ADC_1_InitConfig(&ADC_1_allConfigs[config]);
        #if (ADC_1_VREF_ROUTED)
            ADC_1_vrefAMux_Select((uint8)config);
        #endif
        #if(0u != ADC_1_ANY_CONFIG_USES_FILTER)
            ADC_1_filterVinMux_Select((uint8)config);
            ADC_1_filterInit(config);
        #endif
    	ADC_1_SAR_CHAN_EN_REG = ADC_1_allConfigs[config].chanEn;
    	ADC_1_SAR_RANGE_INTR_MASK_REG = ADC_1_allConfigs[config].rangeIntMask;
    	ADC_1_SAR_SATURATE_INTR_MASK_REG = ADC_1_allConfigs[config].satIntMask;
    	ADC_1_currentConfig = config;
        
        if(1u == restart)
        {
            /* Restart the ADC */    
            ADC_1_Start();
            
            /* Restart the scan */
            ADC_1_StartConvert();
        }
    }
    return;
}

/*******************************************************************************
* Function Name: ADC_1_StartConvert
****************************************************************************//**
*
* \brief In continuous mode, this API starts the conversion process and it runs 
* continuously. 

* In Single Shot mode, the function triggers a single scan and 
* every scan requires a call of this function. The mode is set with the 
* Sample Mode parameter in the customizer. The customizer setting can be 
* overridden at run time with the ADC_1_SetConvertMode() function.
* 
* \param None
* 
* \return None
* 
* \sideeffect None
*
* <b>Code Snippet</b>
*  \snippet main.c snippet_ADC_1_StartConvert
*
*******************************************************************************/
void ADC_1_StartConvert(void)
{
    /* CDT 243358 */
    #if(0u != ADC_1_ANY_CONFIG_USES_FILTER)
        ADC_1_Stop();
        ADC_1_Start();
    #endif
    
	/* Freerunning mode */
	if((ADC_1_SAR_SAMPLE_CTRL_REG 
		& ADC_1_SAR_SAMPLE_CTRL_DSI_TRIGGER_LEVEL_MSK) 
		== ADC_1_SAR_SAMPLE_CTRL_DSI_TRIGGER_LEVEL_MSK)
    {
		ADC_1_SAR_SAMPLE_CTRL_REG |= ADC_1_SAR_SAMPLE_CTRL_CONTINUOUS_MSK ;
	}
	else /* Firmware trigger */
	{
		ADC_1_SAR_START_CTRL_REG = ADC_1_SAR_START_CTRL_FW_TRIGGER_MSK;
	}
    
    return;
}

/*******************************************************************************
* Function Name: ADC_1_StopConvert
****************************************************************************//**
*
* \brief Forces the ADC_1 to stop conversions. If a conversion is 
* currently executing, that conversion will complete, but no further conversions 
* will occur.
* 
* \param None
*
* \return None
*
* \sideeffect None
*
* <b>Code Snippet</b>
*  \snippet main.c snippet_ADC_1_StopConvert
*
*******************************************************************************/
void ADC_1_StopConvert(void)
{
    if((ADC_1_SAR_SAMPLE_CTRL_REG  
		& ADC_1_SAR_SAMPLE_CTRL_DSI_TRIGGER_LEVEL_MSK) 
		== ADC_1_SAR_SAMPLE_CTRL_DSI_TRIGGER_LEVEL_MSK)
	{
        ADC_1_SAR_SAMPLE_CTRL_REG &= ~ADC_1_SAR_SAMPLE_CTRL_CONTINUOUS_MSK;
        
        if((ADC_1_allConfigs[ADC_1_currentConfig].miscConfig 
            & ADC_1_MISC_CONFIG_FILTER_PRESENT_MSK) 
    	    == ADC_1_MISC_CONFIG_FILTER_PRESENT_MSK)
        {
            /* Re-synchronize the Filter if a filter is present */
            ADC_1_Stop();
            ADC_1_Start();
        }
	}
    
    return;
}
