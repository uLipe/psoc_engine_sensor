    /* ****************************************************************************
    * Function Name: ADC_1_TrimFilterVos
    ****************************************************************************//*
    *
    * \brief Runs an algorithm to reduce voltage offset using the UAB's opamp trim.
    *
    * \param None
    * 
    * \return None
    *
    * \sideeffect None
    *
    * <b>Code Snippet</b>
    *  \snippet main.c snippet_ADC_1_TrimFilterVos
    *
    *******************************************************************************/
    void ADC_1_TrimFilterVos(void)
    {
        uint32 opampTrim = 0uL;
        uint32 agndTrim = 0uL;
        uint32 compStatus = 0uL;
        
        ADC_1_SetupComparatorTrim();
        
        /* Sweep from center to crossing */
        compStatus = ADC_1_SetOpampAndAgndTrim(ADC_1_OPAMP_TRIM_POS_BASE, ADC_1_AGND_TRIM_POS_BASE);
        if (0uL == compStatus)
        {
            opampTrim = ADC_1_RaiseOpampTrimToCrossing(ADC_1_OPAMP_TRIM_POS_BASE);
            agndTrim = ADC_1_RaiseAgndTrimToCrossing(ADC_1_AGND_TRIM_POS_BASE);
            if ((agndTrim == (ADC_1_AGND_TRIM_POS_LIMIT - 1uL))
                && (opampTrim != (ADC_1_OPAMP_TRIM_POS_LIMIT - 1uL)))
            {
                opampTrim = ADC_1_IncOpampTrim(opampTrim);
                (void) ADC_1_SetOpampAndAgndTrim(opampTrim, ADC_1_AGND_TRIM_POS_BASE);
                (void) ADC_1_LowerAgndTrimToCrossing(ADC_1_AGND_TRIM_POS_BASE);
            }
        }
        else
        {
            opampTrim = ADC_1_LowerOpampTrimToCrossing(ADC_1_OPAMP_TRIM_POS_BASE);
            agndTrim = ADC_1_LowerAgndTrimToCrossing(ADC_1_AGND_TRIM_POS_BASE);
            if ((agndTrim == (ADC_1_AGND_TRIM_NEG_LIMIT - 1uL))
                && (opampTrim != (ADC_1_OPAMP_TRIM_NEG_LIMIT - 1uL)))
            {
                opampTrim = ADC_1_DecOpampTrim(opampTrim);
                (void) ADC_1_SetOpampAndAgndTrim(opampTrim, ADC_1_AGND_TRIM_POS_BASE);
                (void) ADC_1_RaiseAgndTrimToCrossing(ADC_1_AGND_TRIM_POS_BASE);
            }
        }
        
        ADC_1_UndoComparatorTrimSetup();
    }
    
    
    static void ADC_1_SetupComparatorTrim(void)
    {
        /* Set firmware modbit. */
        ADC_1_UABH_A_CAP_ABCF_VAL_REG |= (0x1uL << CyUAB_SIGN_VAL_SHIFT);
        ADC_1_UABH_B_CAP_ABCF_VAL_REG |= (0x1uL << CyUAB_SIGN_VAL_SHIFT);
        
        /* Open filter output switch to isolate filter during trim. */
        ADC_1_UABH_B_SW_CF_BOT_REG &= ~(uint32)(ADC_1_UABH_B_SW_PO_MASK);
        
        /* Power on the UAB half B reference buffer. */
        ADC_1_UABH_B_OA_CTRL_REG |= (uint32)ADC_1_UABH_B_REF_PWR_MASK;
        
        /* Connect the UAB half B reference buffer to the comparator. */
        ADC_1_UABH_B_SW_STATIC_REG |= (uint32)ADC_1_UABH_B_SW_RT_MASK;
                        
        /* Clock the UAB half B comparator with phi1. */
        ADC_1_UABH_B_SW_OTHER_REG &= ~(uint32)ADC_1_UABH_B_CMP_FF_MASK;
        ADC_1_UABH_B_SW_OTHER_REG |= (uint32)((uint32)CyUAB_CLK_PHI1 << ADC_1_UABH_B_CMP_FF_SHIFT);
        
        /* Power on the UAB half B comparator. */
        ADC_1_UABH_B_OA_CTRL_REG |= (uint32)ADC_1_UABH_B_CMP_PWR_MASK;
    }
    
    
    static void ADC_1_UndoComparatorTrimSetup(void)
    {
        /* Power off the UAB half B comparator. */
        ADC_1_UABH_B_OA_CTRL_REG &= ~(uint32)ADC_1_UABH_B_CMP_PWR_MASK;
        
        /* Remove clock from the UAB half B comparator. */
        ADC_1_UABH_B_SW_OTHER_REG &= ~(uint32)ADC_1_UABH_B_CMP_FF_MASK;
        ADC_1_UABH_B_SW_OTHER_REG |= (uint32)((uint32)CyUAB_SW_OPEN << ADC_1_UABH_B_CMP_FF_SHIFT);
        
        /* Disconnect the UAB half B reference buffer from the comparator. */
        ADC_1_UABH_B_SW_STATIC_REG &= ~(uint32)ADC_1_UABH_B_SW_RT_MASK;
                        
        /* Power off the UAB half B reference buffer. */
        ADC_1_UABH_B_OA_CTRL_REG &= ~(uint32)ADC_1_UABH_B_REF_PWR_MASK;
        
        /* Close filter output switch to connect filter to AROUTE. */
        ADC_1_UABH_B_SW_CF_BOT_REG &= ~(uint32)(ADC_1_UABH_B_SW_PO_MASK);
        ADC_1_UABH_B_SW_CF_BOT_REG |= (uint32)((uint32)CyUAB_CLK_PHI3 << ADC_1_UABH_B_SW_PO_SHIFT);
        
        /* Clear the modbit */
        ADC_1_UABH_A_CAP_ABCF_VAL_REG &= ~(0x1uL << CyUAB_SIGN_VAL_SHIFT);
        ADC_1_UABH_B_CAP_ABCF_VAL_REG &= ~(0x1uL << CyUAB_SIGN_VAL_SHIFT);
    }
    
    
    static uint32 ADC_1_RaiseOpampTrimToCrossing(uint32 startTrim)
    {
        uint32 opampTrim;
        uint32 lastOpampTrim;
        uint32 compStatus;

        lastOpampTrim = startTrim;
        for (opampTrim = startTrim;
            opampTrim != ADC_1_OPAMP_TRIM_NEG_LIMIT;
            opampTrim = ADC_1_IncOpampTrim(opampTrim))
        {
            compStatus = ADC_1_SetOpampAndAgndTrim(opampTrim, ADC_1_AGND_TRIM_POS_BASE);
            if (0uL != compStatus)
            {
                (void) ADC_1_SetOpampTrim(lastOpampTrim);
                break;
            }
            lastOpampTrim = opampTrim;
        }
        
        return lastOpampTrim;
    }


